//对于基于优化的问题，好的初值能够使得优化问题快速收敛并且避免局部最优解
//主要任务：利用cloudInfo.imuRollInit等、cloudInfo.initialGuessX等、cloudInfo.initialGuessRoll等来更新transformTobeMapped[6]，表示先验位姿估计
//也用到了imu原始数据cloudInfo.imuRollInit等来更新lastImuTransformation
    void updateInitialGuess()
    {
        // save current transformation before any processing
        //转换一下数组顺序，同时将格式转换为pcl格式
        //在后边的程序里，前三个值来保存点云数据里的欧拉角
        //transformTobeMapped保存上一帧优化后的最佳位姿
        incrementalOdometryAffineFront = trans2Affine3f(transformTobeMapped);//6个元素的一维float数组，初始化全为0

        static Eigen::Affine3f lastImuTransformation;
        // initialization
        //这里应该是一开始的时候利用imu与一帧雷达对应初始时刻的原始数据初始化transformTobeMapped和lastImuTransformation？
        // 判断关键帧的数量，满足条件表明刚初始化完成，这里仅仅使用imu原始数据
        if (cloudKeyPoses3D->points.empty())
        {
            transformTobeMapped[0] = cloudInfo.imuRollInit;
            transformTobeMapped[1] = cloudInfo.imuPitchInit;
            transformTobeMapped[2] = cloudInfo.imuYawInit;
            //磁力计将yaw对齐
            if (!useImuHeadingInitialization)
                transformTobeMapped[2] = 0;

            lastImuTransformation = pcl::getTransformation(0, 0, 0, cloudInfo.imuRollInit, cloudInfo.imuPitchInit, cloudInfo.imuYawInit); // save imu before return;
            return;
        }

        // use imu pre-integration estimation for pose guess
        static bool lastImuPreTransAvailable = false;
        static Eigen::Affine3f lastImuPreTransformation;
        //这个if条件在预积分模块满足后就为true。当然一开始不满足，只有在地图优化这个节点发布雷达里程计后才满足
        if (cloudInfo.odomAvailable == true)
        {
            Eigen::Affine3f transBack = pcl::getTransformation(cloudInfo.initialGuessX,    cloudInfo.initialGuessY,     cloudInfo.initialGuessZ, 
                                                               cloudInfo.initialGuessRoll, cloudInfo.initialGuessPitch, cloudInfo.initialGuessYaw);
            //表明是第一次接收到
            if (lastImuPreTransAvailable == false)
            {
                lastImuPreTransformation = transBack;
                lastImuPreTransAvailable = true;
            } 
            else
            {
                //lastImuPreTransformation、transBack来自imu里程计，推算出增量transIncre
                //transTobe是上一帧的PCL优化结果
                Eigen::Affine3f transIncre = lastImuPreTransformation.inverse() * transBack;
                Eigen::Affine3f transTobe = trans2Affine3f(transformTobeMapped);//上一帧的最优估计
                Eigen::Affine3f transFinal = transTobe * transIncre;//先验位姿估计=上一帧的最优估计*增量（利用两帧里程计）
                pcl::getTranslationAndEulerAngles(transFinal, transformTobeMapped[3], transformTobeMapped[4], transformTobeMapped[5], 
                                                              transformTobeMapped[0], transformTobeMapped[1], transformTobeMapped[2]);//此时该数组是最新的先验估计
                //保存当前帧里程计信息，用于计算下一帧增量。
                lastImuPreTransformation = transBack;
                //仍然保存磁力计信息
                lastImuTransformation = pcl::getTransformation(0, 0, 0, cloudInfo.imuRollInit, cloudInfo.imuPitchInit, cloudInfo.imuYawInit); // save imu before return;
                return;
            }
        }

        // use imu incremental estimation for pose guess (only rotation)
        //表示没有里程计数据，则使用imu数据，没有平移是因为imu的位移数据不靠谱？
        if (cloudInfo.imuAvailable == true)
        {
            Eigen::Affine3f transBack = pcl::getTransformation(0, 0, 0, cloudInfo.imuRollInit, cloudInfo.imuPitchInit, cloudInfo.imuYawInit);
            Eigen::Affine3f transIncre = lastImuTransformation.inverse() * transBack;

            Eigen::Affine3f transTobe = trans2Affine3f(transformTobeMapped);
            Eigen::Affine3f transFinal = transTobe * transIncre;
            pcl::getTranslationAndEulerAngles(transFinal, transformTobeMapped[3], transformTobeMapped[4], transformTobeMapped[5], 
                                                          transformTobeMapped[0], transformTobeMapped[1], transformTobeMapped[2]);

            lastImuTransformation = pcl::getTransformation(0, 0, 0, cloudInfo.imuRollInit, cloudInfo.imuPitchInit, cloudInfo.imuYawInit); // save imu before return;
            return;
        }
    }

