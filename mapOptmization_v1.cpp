    void laserCloudInfoHandler(const lio_sam::cloud_infoConstPtr& msgIn)
    {
        // extract time stamp
        timeLaserInfoStamp = msgIn->header.stamp;
        timeLaserInfoCur = msgIn->header.stamp.toSec();

        // extract info and feature cloud
        cloudInfo = *msgIn;
        pcl::fromROSMsg(msgIn->cloud_corner,  *laserCloudCornerLast);
        pcl::fromROSMsg(msgIn->cloud_surface, *laserCloudSurfLast);
    /**
        * 一共三个线程使用到这把锁
        * 1. 雷达里程计线程，也就是当前线程
        * 2. 发布全局地图线程，执行关键帧点云拷贝转换操作
        * 3. 回环检测线程，执行关键帧姿态拷贝操作
    */
        std::lock_guard<std::mutex> lock(mtx);

        static double timeLastProcessing = -1;
        if (timeLaserInfoCur - timeLastProcessing >= mappingProcessInterval)//mappingProcessInterval = 0.15s
        {
            timeLastProcessing = timeLaserInfoCur;
//主要任务：利用cloudInfo.imuRollInit等、cloudInfo.initialGuessX等、cloudInfo.initialGuessRoll等来更新transformTobeMapped[6]，表示先验位姿估计
//利用上一帧的最优估计得到当前帧的先验估计
            updateInitialGuess();
//主要任务：创建KD树，下采样，加入局部地图（基于距离和时间），存放到地图容器，主要操作对象是cloudKeyPoses3D
            extractSurroundingKeyFrames();
//主要任务：当前帧角点点云、面点点云降采样
            downsampleCurrentScan();
//点云匹配
//主要任务：对降采样之后的角点、面点构建点到平面、点到直线的残差, 用高斯牛顿法进行优化当前位姿（与先验位姿比较）     得到雷达里程计      
            scan2MapOptimization();
//主要任务：//判断添加里程计因子、GPS因子、回环因子到因子图 利用优化后结果更新3D、6D信息，transformTobeMapped，保存角点、面点关键帧
            saveKeyFramesAndFactor();
//主要任务：根据优化器更新结果isamCurrentEstimate来更新历史帧3D和6D
// 当新的回环因子或者GPS因子加入因子图时，对历史帧执行位姿更新
            correctPoses();
//主要任务：发布激光里程计
            publishOdometry();
//主要任务：发布当前帧对齐到地图坐标系的点云和完整轨迹
            publishFrames();
        }
    }

